class Solution {
  public:
    void sieve() {}

    vector<int> findPrimeFactors(int N) {

        // Write your code here
        vector<int> ans ;
        int number = 2 ;
        for( ; number <= N ; number++)
        {
            while(N%number == 0)
            {
                ans.push_back(number) ;
                N /= number ;
            }
        }
        return ans ;
    }
};

